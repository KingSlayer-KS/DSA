package Basic_stuff;


public class loops {
    public static void main(String[] args) {
        // FOR LOOPS
        for (int i = 100; i >= 0; i = i - 1) {
            System.out.println(i);
        }
        int i = 100;
        // WHILE LOOP
        while (i >= 0) {
            System.out.println(i);
            i = i - 1;
        }
        // DO WHILE LOOP
        int l = 100;

        do {
            System.out.println(i);
            l = l - 1;
        } while (l >= 1);// (;) is termiantor here

    }

}
