package Basic_stuff;

public class Opreators {
    public static void main(String[] args) {
        //Arethmetic operators
        int a=4;
        int b=8;
        int sum= a+b;
        int dif= a-b;
        int mult= a*b;
        int div= a/b;
        int modulo= a%b;//spits remainder left after the division is done
        System.out.println(sum);
        System.out.println(dif);
        System.out.println(mult);
        System.out.println(modulo);
        System.out.println(div);//result will be in int as the specified data type is int

        //urianary operator
        int aa=1;
        System.out.println(aa++);//output=1
        System.out.println(++aa);//output=2
        System.out.println(aa--);//output=1
        System.out.println(--aa);//output=0


    }
}
