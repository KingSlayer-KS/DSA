package Basic_stuff;

public class Maths_class {
    public static void main(String[] args) {
        //some useful operators from class maths
        System.out.println(Math.max(9,6));
        System.out.println(Math.min(9,6));
        System.out.println(Math.random());//prints out random value in range 0.0 > 1.0 output is float/long
        System.out.println((int)(Math.random()*100));//casting the float to int
    }
}
